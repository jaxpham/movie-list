import React from 'react';
import MovieItem from './movieItem.jsx'

const MovieList = (props) => {
  const movies = props.movies;
  // console.log(movies)

  //Check filter the movie actual movie being searched and only show that movie

  const movieItem = movies.map((movie, i) => <MovieItem key={i} movie={movie}/>)
  return (
    <div>
      {movieItem}
    </div>
  );
}

export default MovieList


